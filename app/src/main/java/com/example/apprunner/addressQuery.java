package com.example.apprunner;

public class addressQuery {

    String address;
    String district;
    String mueangDistrict;
    String province;
    String country_number;
    String name;
    String tel;
    String first_name;
    String last_name;
    int id_address;

    public int getId_address(){
        return id_address;
    }

    public void setId_address(){
        this.id_address = id_address;
    }

    public String getAddress(){
        return address;
    }

    public void setAddress(){
        this.address = address;
    }

    public String getDistrict(){
        return district;
    }

    public void setDistrict(){
        this.district = district;
    }

    public String getMueangDistrict(){
        return mueangDistrict;
    }

    public void setMueangDistrict(){
        this.mueangDistrict = mueangDistrict;
    }

    public String getProvince(){
        return province;
    }

    public void setProvince(){
        this.province = province;
    }

    public String getCountry_number(){
        return country_number;
    }

    public void setCountry_number(){
        this.country_number = country_number;
    }

    public String getName(){
        return name;
    }

    public void setName(){
        this.name = name;
    }

    public String getTel(){
        return tel;
    }

    public void setTel(){
        this.tel = tel;
    }

    public String getFirst_name(){
        return first_name;
    }

    public void setFirst_name(){
        this.first_name = first_name;
    }

    public String getLast_name(){
        return last_name;
    }

    public void setLast_name(){
        this.last_name = last_name;
    }
}
